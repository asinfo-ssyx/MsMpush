package com.asiainfo.ssyx.bean;

public class UserViewInfo {

	private String phone_no;
	private String brand_id;
	private Integer new_user_flag;
	private Integer owe_flag;
	private String bill_fee;

	public String getPhone_no() {
		return phone_no;
	}
	public void setPhone_no(String phone_no) {
		this.phone_no = phone_no;
	}
	public String getBrand_id() {
		return brand_id;
	}
	public void setBrand_id(String brand_id) {
		this.brand_id = brand_id;
	}
	public Integer getNew_user_flag() {
		return new_user_flag;
	}
	public void setNew_user_flag(Integer new_user_flag) {
		this.new_user_flag = new_user_flag;
	}
	public Integer getOwe_flag() {
		return owe_flag;
	}
	public void setOwe_flag(Integer owe_flag) {
		this.owe_flag = owe_flag;
	}
	public String getBill_fee() {
		return bill_fee;
	}
	public void setBill_fee(String bill_fee) {
		this.bill_fee = bill_fee;
	}
	
	
	

}
